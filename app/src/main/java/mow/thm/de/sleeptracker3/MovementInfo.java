package mow.thm.de.sleeptracker3;

public class MovementInfo {

    // Mittelwert x-Achse
    private float x;

    // Mittelwert y-Achse
    private float y;

    // Mittelwert z-Achse
    private float z;


    public float getX() {
        return x;
    }
    public float getY() {
        return y;
    }
    public float getZ() {
        return z;
    }

    public void setX(float x) {
        this.x = x;
    }
    public void setY(float y) {
        this.y = y;
    }
    public void setZ(float z) {
        this.z = z;
    }

}
